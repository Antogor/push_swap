#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char *l;

	l = (unsigned char *)s;
	if (n > 0)
	{
		while (n > 0)
		{
			*(l++) = '\0';
			n--;
		}
	}
	else if (n == 0)
	{
	}
}
